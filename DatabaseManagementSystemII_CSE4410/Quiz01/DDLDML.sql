drop table QUIZ_GRADES;
drop table QUIZ_COURSES;
drop table quiz_transactions;
drop table QUIZ_STUDENTS;
DROP table QUIZ_ACCOUNT;

CREATE TABLE QUIZ_ACCOUNT(
    ACCOUNTNO NUMBER PRIMARY KEY,
    ACCOUNTHOLDER VARCHAR2(50)
);

CREATE TABLE QUIZ_STUDENTS(
    ID NUMBER PRIMARY KEY,
    NAME VARCHAR2(50),
    ADDRESS VARCHAR2(100),
    ACCOUNTNO NUMBER,
    FOREIGN KEY (ACCOUNTNO) REFERENCES QUIZ_ACCOUNT(ACCOUNTNO)
);

CREATE TABLE QUIZ_COURSES(
    CID VARCHAR2(10) PRIMARY KEY,
    TITLE VARCHAR2(50),
    CREDIT NUMERIC(5, 0),
    CAT CHAR(2) CHECK ( CAT = '1' OR CAT='0')
);

CREATE TABLE QUIZ_GRADES(
    SID NUMBER,
    CID VARCHAR2(10),
    GRADE VARCHAR2(5),
    PRIMARY KEY (SID, CID),
    FOREIGN KEY (SID) REFERENCES QUIZ_STUDENTS(ID),
    FOREIGN KEY (CID) REFERENCES QUIZ_COURSES(CID)
);

CREATE TABLE QUIZ_TRANSACTIONS(
    ACCNOFROM NUMBER,
    ACCTO NUMBER,
    DATETIME TIMESTAMP,
    AMOUNT NUMERIC(10, 0),
    DESCRIP VARCHAR2(100),
    TTYPE CHAR(2) CHECK(TTYPE = '1' OR TTYPE = '-1'),
    PRIMARY KEY(ACCNOFROM, DATETIME), --assuming it isnt possible for a particular account to perform >1 transaction at the exact moment in time
    FOREIGN KEY(ACCNOFROM) REFERENCES QUIZ_ACCOUNT(ACCOUNTNO)
);


